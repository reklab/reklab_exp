% --- Executes just before CUID_DataSet is made visible.
function CUID_DataSet_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to CUID_DataSet (see VARARGIN)

% Choose default command line output for CUID_DataSet
handles.output = hObject;

 % have to explicitly set background colour for figures
 set(hObject,'Color','white');

    
 directions = 'Select signals to be stored in the flb file';
set(handles.text25, 'String', directions);

% Define channel choices available from popup menus using
% card_channel_def2.txt
fid = fopen('card_channel_def2.txt','r');
M = textscan(fid, '%s', 'commentStyle', '!','delimiter', '|');
M1 = M{1};
m = [];
for i =1:length(M1)
    m = [m M1{i} ';'];
end
eval(m);

strPopup {1} = 'Select a signal';
for i = 1:length(C)
    strPopup {i+1} = C{i};
end

% Assign choices to all popupmenus
for i = 1:32
    evalstr = ['set(handles.popupmenu' num2str(i) ', ''String'', strPopup)'];
    eval(evalstr);
end

% If editing bring up past values

if nargin == 4 %varargin is 4th input argument of the function
     DataSet = varargin{1};
    
    % Set name and pop-up menus to previously defined values
    dName = GetName( DataSet );
    ChannelVector = GetChannelVector( DataSet );
    
    set(handles.editName, 'String' , dName);
    
    for i =1:length(ChannelVector)
        evalstr = ['set(handles.popupmenu' num2str(i) ', ''Value'', ChannelVector(i)+1);'];
        eval(evalstr);
    end
else
    DataSet = [];
end

% Ensure DataSet is of proper class, and create DataSet if none exists
DataSet = Ctrl_DataSet( DataSet );

% assigne handles for DataSet and strPopup
handles.DataSet = DataSet;
handles.strPopup = strPopup;

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes CUID_DataSet wait for user response (see UIRESUME)
uiwait(handles.figure1);



% --- Executes on button press in m_ctrlOK.
function m_ctrlOK_Callback(hObject, eventdata, handles)
% hObject    handle to m_ctrlOK (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Gat name of DataSet
dName = get(handles.editName,'String');

%Collect channel numbers and store in vector
for i = 1:32
    evalstr = ['ChannelVector(i) = get(handles.popupmenu' num2str(i) ', ''Value'');'];
    eval(evalstr);
end

ChannelVector = ChannelVector - 1;

% Remove all trailing empty channels
zeroflag = 0;

while zeroflag == 0
    if isempty(ChannelVector)
        errordlg('Please select a signal','Select a signal','modal');
        return
    else
        if ChannelVector(end) == 0
            ChannelVector = ChannelVector(1:end-1);
        else
            zeroflag = 1;
        end
    end
end

% Check if there remains any empty channels

I = find(ChannelVector == 0);

if isempty(I)
else
    errordlg(['Channel(s) ' num2str(I) ' not defined',],  'Channel Selection Error', 'modal')
    return
end

% Get old DataSet

DataSet = handles.DataSet;

% Create new DataSet using name and ChannelVector
DataSet = SetChannelVector ( DataSet, ChannelVector);
DataSet = SetName ( DataSet, dName);

% Store channel names also for display purposes
strPopup = handles.strPopup;

for i=1:length(ChannelVector)
    ChannelNames{i} = strPopup{ChannelVector(i)+1};
end

DataSet = SetChannelNames ( DataSet, ChannelNames );

% Store new DataSet

handles.output = DataSet;

% Update handles structure
guidata(hObject, handles);

uiresume(handles.figure1); 

